package com.leaf.myapp.vo;

public class SirenCartVO {
    private long od_price;
    private int p_num;
    private int od_cnt;
    private int fc_num;
    private String userid;    
    
	public long getOd_price() {
		return od_price;
	}
	public void setOd_price(long od_price) {
		this.od_price = od_price;
	}
	public int getP_num() {
		return p_num;
	}
	public void setP_num(int p_num) {
		this.p_num = p_num;
	}
	public int getOd_cnt() {
		return od_cnt;
	}
	public void setOd_cnt(int od_cnt) {
		this.od_cnt = od_cnt;
	}
	public int getFc_num() {
		return fc_num;
	}
	public void setFc_num(int fc_num) {
		this.fc_num = fc_num;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
    
    
}
